    private void datatabel(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("ID");
        tbl.addColumn("Jenis Kereta");
        tbl.addColumn("Jumlah Tiket");
        tbl.addColumn("Nama Kereta");
        tbl.addColumn("Biaya");
        tabel_kereta.setModel(tbl);
        
        try{
            Statement input = (Statement) konek.getConnection().createStatement();
            ResultSet res = input.executeQuery("SELECT * FROM kereta");
            while(res.next())
            {
                tbl.addRow(new Object[] {
                res.getString("id"),
                res.getString("jenis"),
                res.getString("jumlah"),        
                res.getString("nama"),
                res.getString("biaya"),
                }
                );
                tabel_kereta.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
