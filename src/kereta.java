    private void tabel_keretaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_keretaMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tabel_kereta.getModel();
        tabel_kereta.getSelectedRow();
        
        Object id_penyewa = model.getValueAt(tabel_kereta.getSelectedRow(),0);
        text_id.setText(String.valueOf(id_penyewa));
        
        Object jenis_kereta = model.getValueAt(tabel_kereta.getSelectedRow(),1);
        jkereta.setSelectedItem(String.valueOf(jenis_kereta));

        Object jumlah_tiket = model.getValueAt(tabel_kereta.getSelectedRow(),2);
        jtiket.setText(String.valueOf(jumlah_tiket));
        
        Object nama_kereta = model.getValueAt(tabel_kereta.getSelectedRow(),3);
        namakereta.setText(String.valueOf(nama_kereta));

        Object biaya_kereta = model.getValueAt(tabel_kereta.getSelectedRow(),4);
        biaya.setText(String.valueOf(biaya_kereta));

        button_update.setEnabled(true);
        button_delete.setEnabled(true);
        button_save.setEnabled(false);
    }//GEN-LAST:event_tabel_keretaMouseClicked

    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        // TODO add your handling code here:
        if(button_update.getText()=="bedit"){
            text_id.requestFocus();
        }else{
            String id_penyewa = text_id.getText();
            String jenis_kereta = (String) jkereta.getSelectedItem();
            String jumlah_tiket = jtiket.getText();
            String nama_kereta = namakereta.getText();
            String biaya_kereta = biaya.getText();
            String hargaa = harga.getText();
            String total = txttotal.getText();
            String bayarr = bayar.getText();
            String kembali = txtkembali.getText();
            try{
                Statement statement = (Statement) konek.getConnection().createStatement();
                statement.executeUpdate("UPDATE kereta SET jenis='"+jenis_kereta+"', jumlah='"+jumlah_tiket+"', nama='"+nama_kereta+"', biaya='"+biaya_kereta+"'"+" WHERE id='"+id_penyewa+"' ");
                JOptionPane.showMessageDialog(null, "Data berhasil di Update");
                clear();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal di Update"+ e.getMessage());
            }
            datatabel();
        }
    }//GEN-LAST:event_button_updateActionPerformed

    private void button_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_deleteActionPerformed
        // TODO add your handling code here:
        String id_penyewa = text_id.getText();
        String jenis_kereta = (String) jkereta.getSelectedItem();
        String jumlah_tiket = jtiket.getText();
        String nama_kereta = namakereta.getText();
        String biaya_kereta = biaya.getText();
        String hargaa = harga.getText();
        String total = txttotal.getText();
        String bayarr = bayar.getText();
        String kembali = txtkembali.getText();
        try{
            Statement statement = (Statement) konek.getConnection().createStatement();
            statement.executeUpdate("DELETE FROM kereta  WHERE id='"+id_penyewa+"' ");
            JOptionPane.showMessageDialog(null, "Berhasil menghapus Data!");
            statement.close();
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Gagal menghapus Data!"+ e.getMessage());
        }
        datatabel();
        clear();
    }//GEN-LAST:event_button_deleteActionPerformed
