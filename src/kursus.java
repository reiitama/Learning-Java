    private void datatabel(){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("Nomor Pendaftaran");
        tbl.addColumn("Jenis Khursus");
        tbl.addColumn("Nama Khursus");
        tbl.addColumn("Hari");
        tbl.addColumn("Total Bayar");
        tabel_komputer.setModel(tbl);
        
        try{
            Statement input = (Statement) konek.getConnection().createStatement();
            ResultSet res = input.executeQuery("SELECT * FROM kursus");
            while(res.next())
            {
                tbl.addRow(new Object[] {
                res.getString("nomorp"),
                res.getString("jenis"),
                res.getString("nama"),        
                res.getString("hari"),
                res.getString("total"),
                }
                );
                tabel_komputer.setModel(tbl);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }
    
    private void combo_hariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_combo_hariActionPerformed
        // TODO add your handling code here:
        int waktukursus = 0;
        if(radio_pagi.isSelected() && combo_jeniskhursus.getSelectedItem()== "U001"){
            radio_malam.setSelected(false);
            waktukursus=50000;
        }else if(radio_pagi.isSelected() && combo_jeniskhursus.getSelectedItem()== "P001"){
            radio_malam.setSelected(false);
            waktukursus=10000;
        }else if(radio_malam.isSelected() && combo_jeniskhursus.getSelectedItem()== "U001"){
            radio_pagi.setSelected(false);
            waktukursus=70000;
        }else if(radio_malam.isSelected() && combo_jeniskhursus.getSelectedItem()== "P001"){
            radio_pagi.setSelected(false);
            waktukursus=120000;
        }

        int hari = Integer.parseInt(String.valueOf(combo_hari.getSelectedItem()));
        int total = waktukursus * hari;
        text_biayakhursus.setText(String.valueOf(total));

        checkbox();

    }//GEN-LAST:event_combo_hariActionPerformed

