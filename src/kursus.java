    private void button_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_updateActionPerformed
        // TODO add your handling code here:
        if(button_update.getText()== "bedit"){
            text_id.requestFocus();
        }else{
            String id_kursus = text_id.getText();
            String jenis_kursus = (String) combo_jeniskhursus.getSelectedItem();
            String nama_kursus = text_nama.getText();
            String hari = (String) combo_hari.getSelectedItem();
            String biaya_kursus = text_biayakhursus.getText();
            String total_pembayaran = text_totalbayar.getText();
            try{
                Statement statement = (Statement) konek.getConnection().createStatement();
                statement.executeUpdate("UPDATE kursus SET jenis='"+jenis_kursus+"', nama='"+nama_kursus+"', hari='"+hari+"', total='"+biaya_kursus+"'"+" WHERE nomorp='"+id_kursus+"' ");
                JOptionPane.showMessageDialog(null, "Data berhasil di Update");
                clear();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, "Data gagal di Update"+ e.getMessage());
            }
            datatabel();
            clear();
        }
    }//GEN-LAST:event_button_updateActionPerformed

