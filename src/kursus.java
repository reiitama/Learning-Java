    private void checkbox(){
        if(check_internet.isSelected()== true && check_konsumsi.isSelected()== false){
            text_biayafasilitas.setText("20000");
        }else if(check_konsumsi.isSelected()== true && check_internet.isSelected()== false){
             text_biayafasilitas.setText("50000");
        }else if(check_internet.isSelected()== true && check_konsumsi.isSelected()== true){
            text_biayafasilitas.setText("70000");
        }else{
            text_biayafasilitas.setText("0");
        }
    }
    
